function setAllFaceDown(){
  var cards = document.querySelectorAll(".yugi-card");
  for (var i = 0; i < cards.length; i++) {
    var card = cards[i];
    selected = card;
    faceChange();
  }
}
function shuffleDeck(){
  var cards = document.querySelectorAll(".yugi-card");
  for (var i = 0; i < cards.length; i++) {
    var card = cards[i];
    selected = card;
    setZ(Math.trunc(Math.random()*9999));
  }
}


